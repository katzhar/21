#include "libft.h"

int		ft_isprime(int c)
{
	int	i;

	if (!c)
		return (0);
	i = 1;
	while (++i <= c)
	{
		if (!(c % i))
			break ;
	}
	if (i == c)
		return (1);
	return (0);
}

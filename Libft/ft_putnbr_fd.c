#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int c;

	c = 0;
	if (n == -2147483648)
		write(fd, "-2147483648", 12);
	if (n >= -2147483647 && n < 0)
	{
		write(fd, "-", 1);
		n = n * (-1);
	}
	if (n >= 0 && n <= 9)
	{
		c = n + '0';
		write(fd, &c, 1);
	}
	if (n > 9 && n <= 2147483647)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
}

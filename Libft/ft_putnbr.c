#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	c = 0;
	if (n == -2147483648)
		write(1, "-2147483648", 12);
	if (n >= -2147483647 && n < 0)
	{
		write(1, "-", 1);
		n = n * (-1);
	}
	if (n >= 0 && n <= 9)
	{
		c = n + '0';
		write(1, &c, 1);
	}
	if (n > 9 && n <= 2147483647)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
